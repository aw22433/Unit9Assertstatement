
# Python String Operations
str1 = 'Hello'
str2 ='World!'
assert type(str1)==str,"Error with str1"
assert type(str2)==str,"Error with str2"


# using +
print('str1 + str2 = ', str1 + str2)
assert type(str1+str2) == str, "Error with str1+str2"

# using *
print('str1 * 3 =', str1 * 3)
assert type(str1*3)== str, "Error with str1*3"
